#include <iostream>

using namespace std;

int main(void)

{
    int num1 = 10, num2;
    num2 = 15;
    double float_num = 3.14;
    char my_char = 'S';
    string my_string = "Hello World!";

    cout << "num1 = " << num1 << endl;
    cout << "num2 = " << num2 << endl;
    cout << "float_num = " << float_num << endl;
    cout << "my_char = " << my_char << endl;
    cout << "my_string = " << my_string << endl;


    int my_age = 24;
    cout << "I am " << my_age << " years old!" << endl;


    int x = 5;
    int y = 6;
    int z = x+y;
    cout << "x = " << x << endl;
    cout << "y = " << y << endl;
    cout << "z = " << z << endl;
    cout << "x+y+z = " << x+y+z << endl;


    return 0;
}